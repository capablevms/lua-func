extern const char * getfuncname(lua_State *, CallInfo *, const char **);
extern int getcurrentline(CallInfo *);
extern void funcinfo(lua_Debug *, Closure *);

static int auxgetinfo (lua_State *L, const char *what, lua_Debug *ar,
                       Closure *f, CallInfo *ci) {
  int status = 1;
  for (; *what; what++) {
    switch (*what) {
      case 'S': {
        funcinfo(ar, f);
        break;
      }
      case 'l': {
        ar->currentline = (ci && (!((ci)->callstatus & (1<<1)))) ? getcurrentline(ci) : -1;
        break;
      }
      case 'u': {
        ar->nups = (f == ((void*)0)) ? 0 : f->c.nupvalues;
        if (((f) == ((void*)0) || (f)->c.tt == ((6) | ((2) << 4)))) {
          ar->isvararg = 1;
          ar->nparams = 0;
        }
        else {
          ar->isvararg = f->l.p->is_vararg;
          ar->nparams = f->l.p->numparams;
        }
        break;
      }
      case 't': {
        ar->istailcall = (ci) ? ci->callstatus & (1<<5) : 0;
        break;
      }
      case 'n': {
        ar->namewhat = getfuncname(L, ci, &ar->name);
        if (ar->namewhat == ((void*)0)) {
          ar->namewhat = "";
          ar->name = ((void*)0);
        }
        break;
      }
      case 'r': {
        if (ci == ((void*)0) || !(ci->callstatus & (1<<8)))
          ar->ftransfer = ar->ntransfer = 0;
        else {
          ar->ftransfer = ci->u2.transferinfo.ftransfer;
          ar->ntransfer = ci->u2.transferinfo.ntransfer;
        }
        break;
      }
      case 'L':
      case 'f':
        break;
      default: status = 0;
    }
  }
  return status;
}