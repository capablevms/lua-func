extern unsigned int luaS_hash(const char *, size_t, unsigned int);

unsigned int luaS_hashlongstr (TString *ts) {
  ((void)0);
  if (ts->extra == 0) {
    size_t len = ts->u.lnglen;
    ts->hash = luaS_hash(((ts)->contents), len, ts->hash);
    ts->extra = 1;
  }
  return ts->hash;
}