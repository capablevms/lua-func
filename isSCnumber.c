static int isSCnumber (expdesc *e, int *pi, int *isfloat) {
  lua_Integer i;
  if (e->k == VKINT)
    i = e->u.ival;
  else if (e->k == VKFLT && luaV_flttointeger(e->u.nval, &i, F2Ieq))
    *isfloat = 1;
  else
    return 0;
  if (!((e)->t != (e)->f) && fitsC(i)) {
    *pi = ((((int)((i)))) + (((1<<8)-1) >> 1));
    return 1;
  }
  else
    return 0;
}