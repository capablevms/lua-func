typedef struct MatchState {
  const char *src_init;
  const char *src_end;
  const char *p_end;
  lua_State *L;
  int matchdepth;
  unsigned char level;
  struct {
    const char *init;
    ptrdiff_t len;
  } capture[32];
} MatchState;


extern int matchbracketclass(int, const char *, const char *);
extern int match_class(int, int);

extern int singlematch (MatchState *ms, const char *s, const char *p,
                        const char *ep) {
  if (s >= ms->src_end)
    return 0;
  else {
    int c = ((unsigned char)(*s));
    switch (*p) {
      case '.': return 1;
      case '%': return match_class(c, ((unsigned char)(*(p+1))));
      case '[': return matchbracketclass(c, p, ep-1);
      default: return (((unsigned char)(*p)) == c);
    }
  }
}