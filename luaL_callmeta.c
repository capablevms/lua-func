extern void lua_callk(lua_State *, int, int, lua_KContext, lua_KFunction);
extern void lua_pushvalue(lua_State *, int);
extern int luaL_getmetafield(lua_State *, int, const char *);
extern int lua_absindex(lua_State *, int);

extern int luaL_callmeta (lua_State *L, int obj, const char *event) {
  obj = lua_absindex(L, obj);
  if (luaL_getmetafield(L, obj, event) == 0)
    return 0;
  lua_pushvalue(L, obj);
  lua_callk(L, (1), (1), 0, ((void*)0));
  return 1;
}