extern void luaX_syntaxerror(LexState *, const char *);
extern void singlevar(LexState *, expdesc *);
extern void luaK_dischargevars(FuncState *, expdesc *);
extern void check_match(LexState *, int, int, int);
extern void expr(LexState *, expdesc *);
extern void luaX_next(LexState *);

static void primaryexp (LexState *ls, expdesc *v) {

  switch (ls->t.token) {
    case '(': {
      int line = ls->linenumber;
      luaX_next(ls);
      expr(ls, v);
      check_match(ls, ')', '(', line);
      luaK_dischargevars(ls->fs, v);
      return;
    }
    case TK_NAME: {
      singlevar(ls, v);
      return;
    }
    default: {
      luaX_syntaxerror(ls, "unexpected symbol");
    }
  }
}