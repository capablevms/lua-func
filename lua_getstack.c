extern int lua_getstack (lua_State *L, int level, lua_Debug *ar) {
  int status;
  CallInfo *ci;
  if (level < 0) return 0;
  ((void) 0);
  for (ci = L->ci; level > 0 && ci != &L->base_ci; ci = ci->previous)
    level--;
  if (level == 0 && ci != &L->base_ci) {
    status = 1;
    ar->i_ci = ci;
  }
  else status = 0;
  ((void) 0);
  return status;
}