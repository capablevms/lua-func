static void separatetobefnz (global_State *g, int all) {
  GCObject *curr;
  GCObject **p = &g->finobj;
  GCObject **lastnext = findlast(&g->tobefnz);
  while ((curr = *p) != g->finobjold1) {
    ((void)0);
    if (!((((curr)->marked) & (((1<<(3)) | (1<<(4))))) || all))
      p = &curr->next;
    else {
      if (curr == g->finobjsur)
        g->finobjsur = curr->next;
      *p = curr->next;
      curr->next = *lastnext;
      *lastnext = curr;
      lastnext = &curr->next;
    }
  }
}