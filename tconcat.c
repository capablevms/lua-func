extern void checktab (lua_State *, int, int);
extern void addfield (lua_State *, luaL_Buffer *, lua_Integer);

extern int tconcat (lua_State *L) {
  luaL_Buffer b;
  lua_Integer last = (checktab(L, 1, (1) | 4), luaL_len(L, 1));
  size_t lsep;
  const char *sep = luaL_optlstring(L, 2, "", &lsep);
  lua_Integer i = luaL_optinteger(L, 3, 1);
  last = luaL_optinteger(L, 4, last);
  luaL_buffinit(L, &b);
  for (; i < last; i++) {
    addfield(L, &b, i);
    luaL_addlstring(&b, sep, lsep);
  }
  if (i == last)
    addfield(L, &b, i);
  luaL_pushresult(&b);
  return 1;
}
